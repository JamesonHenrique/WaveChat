package com.jhcs.wavechat.infrastructure.notification;

/**
 * Enumeração que representa os diferentes tipos de notificações.
 */
public enum NotificationType {

    /**
     * Notificação de mensagem visualizada.
     */
    SEEN,

    /**
     * Notificação de nova mensagem.
     */
    MESSAGE,

    /**
     * Notificação de imagem.
     */
    IMAGE,

    /**
     * Notificação de áudio.
     */
    AUDIO,

    /**
     * Notificação de vídeo.
     */
    VIDEO,

}